
print("AとBが等しい(数字)")
print(1 == 1) # True 記号で
print(1 == 2) # False 記号で
print(1 is 1) # True 文字で
print(1 is 2) # False 文字で

print("AとBが等しい(文字列)")
print("山田" == "山田") # True 記号で
print("山田" == "田中") # False 記号で
print("山田" is "山田") # True 文字で
print("山田" is "田中") # False 文字で

print("AとBが異なる")
print(1 != 1) # False 記号で
print(1 != 2) # True 記号で
print(1 is not 1) #  False 文字で
print(1 is not 2) # True 文字で